using RogueLib.Entities;

namespace ScreenDemo
{
    public class GameMap
    {
        public string MapText { get; }

        public GameMap()
        {
            MapText =
                """
                          ┌──────┐          ┌─────────────┐            ┌───────┐
                          │......│        ##+.............│            │.......│
                          │......│        # │.............+##          │.......│
                          │......+######### └──────────+──┘ ###########+.......│
                          │......│                     #               └───────┘
                          └──+───┘                     #
                      ########                 ##########
                 ┌────+┐                     ┌─+───────┐              ┌──────────────────┐
                 │.....│                     │.........│              │..................│
                 │.....+#####################+.........│              │..................│
                 │.....│                     │.........│              │..................│
                 │.....│                     │.........│              │..................│
                 │.....│                     │.........+##############+..................│
                 └─+───┘                     └───+─────┘              └───────────────+──┘
                   #                             #                                    #
                   ######               ┌────────+──────────────┐                     #
                        #             ##+.......................│                     #
                        #             # │.......................│   ###################
                        #             # │.......................│   #
                        #             # │.......................+####
                        #             # └───────────────────────┘
                        ###############
                """;
        }

        public bool IsWalkable(Vector2 pos)
        {
            string[] lines = MapText.Replace("\r", "").Split('\n');

            if (pos.Y < 0 || pos.Y >= lines.Length)
                return false;

            if (pos.X < 0 || pos.X >= lines[pos.Y].Length)
                return false;

            char tile = lines[pos.Y][pos.X];


            return tile == '.' || tile == '#' || tile == '+';
        }
    }
}